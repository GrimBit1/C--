// There are 2 types of header files

// 1. System Header Files : It Comes with Compiler
#include <iostream>
// 2. User Defined Header Files : Written By programmer
#include "this.h"

using namespace std;

int main()
{
    int a = 4, b = 3;
    cout << "Operator in C++" << endl;
    cout << "Arithmetic Operator" << endl;
    cout << "a + b :" << a + b << endl;
    cout << "a - b :" << a - b << endl;
    cout << "a * b :" << a * b << endl;
    cout << "a / b :" << a / b << endl;
    cout << "a % b :" << a % b << endl;
    cout << "a++ :" << a++ << endl;
    cout << "a-- :" << a-- << endl;
    cout << "--a :" << --a << endl;
    cout << "++a :" << ++a << endl;

    // Assignment Operator
    // Comparison Operator
    // Logical Operator
}